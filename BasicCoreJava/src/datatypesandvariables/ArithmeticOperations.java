package datatypesandvariables;

import org.omg.Messaging.SyncScopeHelper;

public class ArithmeticOperations {

	public static void main(String[] args)
	{
		int a=10;
		int b=20;
		//Addition
		System.out.println(a+b);
		//Subtsraction
		System.out.println(b-a);
		//Multiplication
		System.out.println(a*b);
		//Division
		System.out.println(a/b);
		//mod remainder
		System.out.println(a%b);

	}

}
